class Product {
  final int id;
  final String nombre;
  final double precio;
  final int marcaId;
  final String marca;
  final int categoriaId;
  final String categoria;
  final String image;

  Product(this.id, this.nombre, this.precio, this.marcaId, this.marca,
      this.categoriaId, this.categoria, this.image);
}
